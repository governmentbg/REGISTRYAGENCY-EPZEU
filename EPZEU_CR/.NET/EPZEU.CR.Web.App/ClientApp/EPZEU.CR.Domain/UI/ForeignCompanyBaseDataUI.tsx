import * as React from "react";
import { ObjectHelper } from "Cnsys.Core";
import { BaseProps } from "Cnsys.UI.React";
import { EPZEUBaseComponent, ValidationSummary, ValidationSummaryStrategy } from "EPZEU.Core";
import { ForeignCompanyBaseData } from "../Models/ModelsAutoGenerated";

interface ForeignCompanyBaseDataProps extends BaseProps {
    companyNameResourceKey?: string;
}

export class ForeignCompanyBaseDataUI extends EPZEUBaseComponent<ForeignCompanyBaseDataProps, ForeignCompanyBaseData> {

    renderEdit(): JSX.Element {
        return <>
            <ValidationSummary {...this.props} strategy={ValidationSummaryStrategy.includeOnlyModelErrors} includeErrorsRecursive={true} />
            <div className="row">
                <div className="form-group col-12">
                    {this.labelFor(m => m.companyName, this.props.companyNameResourceKey ? this.props.companyNameResourceKey : 'CR_GL_00005_L')}
                    {this.textBoxFor(m => m.companyName)}
                </div>
            </div>
            <div className="row">
                <div className="form-group col-md-6">
                    {this.labelFor(m => m.competentAuthorityForRegistration, 'CR_GL_AUTHORITY_REGISTRATION_L')}
                    {this.textBoxFor(m => m.competentAuthorityForRegistration)}
                </div>
                <div className="form-group col-md-6">
                    {this.labelFor(m => m.registrationNumber, 'CR_GL_REGISTRATION_NUMBER_L')}
                    {this.textBoxFor(m => m.registrationNumber)}
                </div>
            </div>
        </>
    }

    renderDisplay(): JSX.Element {
        return <div>
            {!ObjectHelper.isStringNullOrEmpty(this.model.companyName) ? <div>{this.getResource(this.props.companyNameResourceKey ? this.props.companyNameResourceKey : 'CR_GL_00005_L')}: {this.model.companyName}</div> : null}
            {!ObjectHelper.isStringNullOrEmpty(this.model.competentAuthorityForRegistration) ? <div>{this.getResource('CR_GL_AUTHORITY_REGISTRATION_L')}: {this.model.competentAuthorityForRegistration}</div> : null}
            {!ObjectHelper.isStringNullOrEmpty(this.model.registrationNumber) ? <div>{this.getResource('CR_GL_REGISTRATION_NUMBER_L')}: {this.model.registrationNumber}</div> : null}
        </div>
    }
}