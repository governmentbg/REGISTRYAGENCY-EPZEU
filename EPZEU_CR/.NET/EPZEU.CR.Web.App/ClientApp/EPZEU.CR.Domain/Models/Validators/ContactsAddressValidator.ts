import { EPZEUBaseValidator } from 'EPZEU.Core';
import { Contacts } from '../ModelsAutoGenerated';
import { IApplicationFormValidationContext } from './ApplicationFormValidationContext';

export class ContactsAddressValidator extends EPZEUBaseValidator<Contacts, IApplicationFormValidationContext> {
    constructor() {
        super();

        this.ruleFor(m => m.eMail).multiEmailAddresses().withMessage(this.getMessage('GL_INPUT_VALID_EMAIL_E'));
    }

    public validate(obj: Contacts): boolean {
     
        let isValid = super.validate(obj);

        return isValid;
    }
}