import { EPZEUBaseValidator } from 'EPZEU.Core'
import { ApplicantInfo } from '../ModelsAutoGenerated';
import { IApplicationFormValidationContext } from './ApplicationFormValidationContext';
import { ApplicantsValidator } from './ApplicantValidator'
import { ApplicantCapacityValidator } from './ApplicantCapacityValidator'

export class ApplicantInfoValidator extends EPZEUBaseValidator<ApplicantInfo, IApplicationFormValidationContext> {
    constructor() {
        super();
        this.ruleFor(m => m.applicants).setValidator(new ApplicantsValidator());
        this.ruleFor(m => m.applicantCapacity).setValidator(new ApplicantCapacityValidator());
    }

    public validate(obj: ApplicantInfo): boolean {
        let isValid = super.validate(obj);

        return isValid;
    }
}