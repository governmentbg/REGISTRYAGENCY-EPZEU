import { EPZEUBaseValidator } from 'EPZEU.Core';
import { IApplicationFormValidationContext } from '../..';
import { Price } from '../ModelsAutoGenerated';

export class PriceValidator extends EPZEUBaseValidator<Price, IApplicationFormValidationContext> {
    constructor() {
        super();
        // Валидни варианти:
        //      - само цифри - 232323
        //      - цифри, последвани от точка и една цифра - 232323.2
        //      - цифри, последвани от точка и две цифри - 232323.23
        this.ruleFor(m => m.amount).matches('^\\s*\\d+(\\.\\d)*\\s*$|^\\s*\\d+(\\.\\d\\d)*\\s*$').withMessage(this.getMessage('GL_INPUT_VALUE_E')); // Въведете стойност
    }

    public validate(obj: Price): boolean {
        let isValid = super.validate(obj);

        

        return isValid;
    }
}