import * as React from "react";
import { observer } from "mobx-react";
import { EPZEUBaseComponent } from 'EPZEU.Core';
import { FieldContainerProps, withFieldRecordContainer } from 'EPZEU.CR.Domain';
import { F325a_PartialEraseDistraint } from '../../Models/Fields/ModelsAutoGenerated';
import { ObjectHelper } from "Cnsys.Core";

@observer class F325a_PartialEraseDistraintUI extends EPZEUBaseComponent<FieldContainerProps, F325a_PartialEraseDistraint> {

    constructor(props?: FieldContainerProps) {
        super(props);
    }

    renderEdit(): JSX.Element {
        return <>
            <div className="row">
                <div className="form-group col-12">
                    <div className="custom-control custom-checkbox">
                        {this.checkBoxFor(m => m.checked, "CR_APP_PARTIALLY_ERASED_PLEDGE_L" )}
                    </div>
                </div>
            </div>

            {this.model.checked ? <><div className="row">
                <div className="form-group col-12">
                    {this.labelFor(m => m.description, 'GL_DESCRIPTION_L')}
                    {this.textAreaFor(m => m.description)}
                </div>
            </div>
            </>
                : null}
        </>
    }

    renderDisplay(): JSX.Element {
        return (<>
            {this.model.checked &&
                <>{this.getResource('GL_OK_L')}</>}

            {!ObjectHelper.isStringNullOrEmpty(this.model.description) ? <><br />{this.getResource('GL_DESCRIPTION_L')}: </> : null}
            {this.model.description}
        </>);
    }
}

export const F325a_PartialEraseDistraintFieldUI = withFieldRecordContainer(F325a_PartialEraseDistraintUI, {
    fieldLabelTextKey: "CR_F_325a_L",
    fieldLabelFor: (model: F325a_PartialEraseDistraint) => model.checked
});