import { PriceValidator, RecordValidator } from 'EPZEU.CR.Domain';
import { F409_Size409 } from '../../Fields/ModelsAutoGenerated';

export class F409_Size409UIValidator extends RecordValidator<F409_Size409> {
    constructor() {
        super();

        this.ruleFor(m => m.price).setValidator(new PriceValidator);
    }

    public validateInternal(obj: F409_Size409): boolean {
        let isValid = super.validateInternal(obj);

        if (obj.price.amount && !obj.price.units) {
            obj.addError(this.getMessage('GL_INPUT_FIELD_MUST_E')); // Полето е задължително
            isValid = false;
        }

        return isValid;
    }
}