import { RecordValidator } from 'EPZEU.CR.Domain';
import { F311_Description } from '../ModelsAutoGenerated';

export class F311_DescriptionValidator extends RecordValidator<F311_Description> {

    constructor() {
        super();
    }

    public validateInternal(obj: F311_Description): boolean {
        let isValid = super.validateInternal(obj);

        if (!obj.wholeCompany && !obj.partOfCompany) {
            obj.addError(this.getMessage('GL_INPUT_FIELD_MUST_E'))
                isValid = false;
        }

        return isValid;
    }
}