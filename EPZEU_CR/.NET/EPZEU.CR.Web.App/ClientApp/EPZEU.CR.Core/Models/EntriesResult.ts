import { observable } from 'mobx';
import { TypeSystem, BaseDataModel } from 'Cnsys.Core';
import { moduleContext } from '../ModuleContext';
import { Entry } from './ModelsAutoGenerated';

@TypeSystem.typeDecorator('EntriesResult', moduleContext.moduleName)
export class EntriesResult extends BaseDataModel {

    @observable private _items: Entry[] = null;

    constructor(obj?: any) {
        super(obj)

        this.copyFrom(obj);
    }

    @TypeSystem.propertyArrayDecorator(Entry ? Entry : moduleContext.moduleName + '.' + 'Entry')
    public set items(val: Entry[]) {
        this._items = val;
    }

    public get items(): Entry[] {
        return this._items;
    }
}