import { ErrorLevels } from 'Cnsys.Core';
import { ApplicationFormBaseValidator } from 'EPZEU.CR.Domain';
import { ApplicationFormVBase } from '../ApplicationFormsV';
import { ApplicationFormVFieldsBase } from '../ModelsAutoGenerated';
    
export class ApplicationFormVValidator<TApplication extends ApplicationFormVBase<ApplicationFormVFieldsBase>> extends ApplicationFormBaseValidator<TApplication> {

    public validate(obj: TApplication): boolean {
        let isValid = super.validate(obj);

        if (obj.documents == null || obj.documents.length == 0) {
            obj.addError('documents', this.getMessage('GL_NOATTACHED_DOCUMENTS_E'), ErrorLevels.Error);
            isValid = false;
        }

        return isValid;
    }
}