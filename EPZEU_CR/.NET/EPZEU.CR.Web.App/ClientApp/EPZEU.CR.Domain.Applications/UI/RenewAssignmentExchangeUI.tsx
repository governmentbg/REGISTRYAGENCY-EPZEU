import { BaseProps } from "Cnsys.UI.React";
import { EPZEUBaseComponent } from 'EPZEU.Core';
import { RenewAssignmentExchange } from '../Models/ModelsAutoGenerated';
import * as React from "react";

interface RenewAssignmentExchangeProps extends BaseProps {
    disabledCheckBox?: boolean;
    isInitialChecked?: boolean;
}

export class RenewAssignmentExchangeUI extends EPZEUBaseComponent<RenewAssignmentExchangeProps, RenewAssignmentExchange> {
    constructor(props: any) {
        super(props);

        if (this.props.isInitialChecked)
            this.model.cheked = this.props.isInitialChecked 
    }

    renderEdit() {
        return <div className="field-container">
            <div className="row">
                <div className="form-group col">
                    <div className="custom-control custom-checkbox">
                        {this.props.disabledCheckBox
                            ? <>
                                <input type="checkbox" name="application.documents.isOriginal" id="change-correspondence-address" className="custom-control-input" disabled={true} checked={this.model.cheked} />
                                <label htmlFor="change-correspondence-address" className="custom-control-label">{this.getResource('CR_APP_CHANGE_CORRESPONDENCE_ADDRESS_L')}</label>
                            </>
                            : this.checkBoxFor(m => m.cheked, 'CR_APP_CHANGE_CORRESPONDENCE_ADDRESS_L')}
                    </div>
                </div>
            </div>
        </div>
    }

    renderDisplay() {
        return <div className="field-container">
            <div className="row">
                <div className="form-group col">{this.model.cheked ? <div>{this.getResource('CR_APP_CHANGE_CORRESPONDENCE_ADDRESS_L')}</div> : null}
                </div>
            </div>
        </div>
    }
}