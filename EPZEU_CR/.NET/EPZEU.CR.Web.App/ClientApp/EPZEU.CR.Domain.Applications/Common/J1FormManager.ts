import { Address, ApplicantCapacityType, ApplicationFormBaseManager, DeedsDataService } from 'EPZEU.CR.Domain';
import { J1 } from '../Models/ModelsAutoGenerated';

export class J1FormManager extends ApplicationFormBaseManager<J1> {

    public get canFillAppExchangeAddress(): boolean {
        return true;
    }

    public getSeatAddress(): Promise<Address> {
        return new DeedsDataService().getCompanySeat(this.application.uic.text).then((seat) => {
            if (seat)
                return JSON.parse(JSON.stringify(seat.address));

            return null;
        });
    }

    protected createApplication(obj: any): J1 {
        return new J1(obj);
    }
}