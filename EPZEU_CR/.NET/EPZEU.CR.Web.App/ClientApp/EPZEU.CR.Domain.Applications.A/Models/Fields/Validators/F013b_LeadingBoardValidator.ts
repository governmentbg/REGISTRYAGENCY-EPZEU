export class F013b_LeadingBoardValidator extends EPZEUBaseValidator<F013b_LeadingBoard, IApplicationFormValidationContext> {
    constructor() {
        super();

        this.ruleFor(m => m.leadingBoardMandate).setValidator(new F01330_LeadingBoardMandateValidator());
        this.ruleFor(m => m.leaderList).setCollectionValidator(new F01331_LeaderValidator());
    }

    public validate(obj: F013b_LeadingBoard): boolean {
        let isValid = super.validate(obj);

        if (obj.leaderList.length > 1) {
            for (var i = 0; i < obj.leaderList.length; i++) {

                if (ValidatorHelpers.isObjectWithPersonEmpty(obj.leaderList[i], "subject") && obj.leaderList[i].recordOperation != RecordOperations.Erase) {
                    obj.leaderList[i].clearErrors(true);
                    obj.leaderList[i].addError(this.getMessage('CR_APP_00063_E')); //Попълнете данните за полето или го изтрийте.

                    isValid = false;
                }
            }

            //Грешката се визуалзира при добавяне на второ и следващо лице със същия идентификатор като на вече добавено лице
            if (ValidatorHelpers.isExistDublicateIndents(obj.leaderList, false, "CR_APP_00123_E")) // 
                isValid = false;
        }

        return isValid;
    }
}