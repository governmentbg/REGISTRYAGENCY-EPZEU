import { EPZEUBaseComponent, Nomenclatures, ValidationSummaryStrategy } from 'EPZEU.Core';
import { ListRecordsContainerProps, Person, PersonTypes, PersonUI, withListRecordsContainer } from 'EPZEU.CR.Domain';
import * as React from "react";
import { F01331_Leader } from '../../Models/Fields/ModelsAutoGenerated';

class F01331_LeaderUIImpl extends EPZEUBaseComponent<ListRecordsContainerProps, F01331_Leader> {

    renderEdit(): JSX.Element {
        return (<PersonUI {...this.bind(m => m.subject)} PersonType={PersonTypes.Director} />);
    }

    renderDisplay(): JSX.Element {
        return (<PersonUI {...this.bind(m => m.subject)} PersonType={PersonTypes.Director} />);
    }
}

export const F01331_LeaderUI = withListRecordsContainer(F01331_LeaderUIImpl, F01331_Leader, {
    addButtonLabelKey: "CR_APP_ADD_DIRECTORIAL_BOARD_MEMBER_L",
    hasAtLeastOneRecord: true,
    valSummaryStrategy: ValidationSummaryStrategy.excludeAllExcept,
    valSummaryRecursive: true,
    valSummaryPropNames: ["", "subject."],
    newRecordCtor: () => {
        return Nomenclatures.getBGCountry().then(bgCountry => {
            var leadingBoard = new F01331_Leader();

            leadingBoard.subject = new Person();
            leadingBoard.subject.countryCode = bgCountry.code;
            leadingBoard.subject.countryID = bgCountry.id;
            leadingBoard.subject.countryName = bgCountry.name;

            return leadingBoard;
        })
    }
})